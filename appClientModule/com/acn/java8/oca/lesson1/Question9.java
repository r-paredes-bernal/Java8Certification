public class Question9 {

	// Add a ternary statement that do the same conditional operation
	public static void main(String[] args) {
		int x = 10;
		if ( x > 10 ) {
			System.out.println(">");
		} else if ( x < 10 ) {
			System.out.println("<");
		} else {
			System.out.println("=");
		}
	}
}
